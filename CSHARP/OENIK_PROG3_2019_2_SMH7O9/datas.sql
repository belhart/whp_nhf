Create table Csapat(
    csapat_nev varchar(50),
    motor varchar(50),
    versenyek_szama int,
    gyozelmek int,
    constraint n_pk primary key (csapat_nev)
);

create table Versenyzo(
    rajtszam int,
    nev varchar(40) not null,
    csapat_nev varchar(50),
    eletkor int not null,
    ossz_pont int,
    idenybeli_pont int,
    constraint v_pk primary key (rajtszam),
    constraint n_fk foreign key (csapat_nev) references Csapat (csapat_nev),
    constraint op_ck check(ossz_pont > -1),
    constraint ip_ck check(idenybeli_pont > -1)
);


create table Versenyhetvege(
    nev varchar(50) not null,
    VERSENYHETVEGE_SZAMA int primary key,
    hossz int not null,
    kor int not null,
    idopont datetime not null,
    helyszin varchar(30)
);


create table Eredmeny(
    versenyhetvege_szam int,
    rajtszam int,
    helyezes int,
    pont int,
    constraint e_pk primary key (versenyhetvege_szam, rajtszam),
    constraint e_k1 foreign key (versenyhetvege_szam) references Versenyhetvege(VERSENYHETVEGE_SZAMA),
    constraint e_k2 foreign key (rajtszam) references Versenyzo (rajtszam)
);

